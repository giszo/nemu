#include <nemu/memory/rom.h>

#include <cassert>

using memory::ROM;

// =====================================================================================================================
ROM::ROM(uint8_t* data, unsigned size)
    : m_data(data),
      m_size(size)
{
}

// =====================================================================================================================
ROM::~ROM()
{
    delete[] m_data;
}

// =====================================================================================================================
unsigned ROM::size() const
{
    return m_size;
}

// =====================================================================================================================
uint8_t ROM::read(uint16_t address)
{
    assert(address < m_size);
    return m_data[address];
}

// =====================================================================================================================
void ROM::write(uint16_t address, uint8_t data)
{
    throw "trying to write ROM";
}
